CREATE TABLE CURSOS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR2(150 CHAR) NOT NULL,
    DESCRIPCION VARCHAR2(500 CHAR) NOT NULL,
    COSTO NUMBER(12,2) NOT NULL,
    ACTIVO NUMBER(1) DEFAULT 1 NOT NULL,
    FECHA_CREACION TIMESTAMP NOT NULL
);

CREATE TABLE ESTUDIANTES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR2(150 CHAR) NOT NULL,
    CORREO VARCHAR2(180 CHAR) NOT NULL UNIQUE,
    IDENTIFICADOR_IDAAS VARCHAR2(200 CHAR) NOT NULL UNIQUE,
    ACTIVO NUMBER(1) DEFAULT 1 NOT NULL
);

CREATE TABLE INSCRIPCIONES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ESTUDIANTE_ID NUMBER NOT NULL,
    FECHA_INSCRIPCION TIMESTAMP NOT NULL,
    TOTAL NUMBER(12,2) NOT NULL,
    ESTADO VARCHAR2(50 CHAR) NOT NULL,
    CONSTRAINT FK_INSCRIPCIONES_ESTUDIANTE
        FOREIGN KEY (ESTUDIANTE_ID) REFERENCES ESTUDIANTES(ID)
);

CREATE TABLE DETALLE_INSCRIPCION (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    INSCRIPCION_ID NUMBER NOT NULL,
    CURSO_ID NUMBER NOT NULL,
    COSTO_CURSO NUMBER(12,2) NOT NULL,
    CONSTRAINT FK_DETALLE_INSCRIPCION_INSCRIPCION
        FOREIGN KEY (INSCRIPCION_ID) REFERENCES INSCRIPCIONES(ID),
    CONSTRAINT FK_DETALLE_INSCRIPCION_CURSO
        FOREIGN KEY (CURSO_ID) REFERENCES CURSOS(ID)
);

CREATE TABLE CONTENIDOS_CURSO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CURSO_ID NUMBER NOT NULL,
    TITULO VARCHAR2(150 CHAR) NOT NULL,
    DESCRIPCION VARCHAR2(500 CHAR) NOT NULL,
    NOMBRE_ARCHIVO VARCHAR2(200 CHAR) NOT NULL,
    TIPO_CONTENIDO VARCHAR2(120 CHAR) NOT NULL,
    BUCKET_S3 VARCHAR2(180 CHAR),
    KEY_S3 VARCHAR2(400 CHAR),
    RUTA_TEMPORAL_EFS VARCHAR2(400 CHAR) NOT NULL,
    FECHA_CREACION TIMESTAMP NOT NULL,
    CONSTRAINT FK_CONTENIDO_CURSO
        FOREIGN KEY (CURSO_ID) REFERENCES CURSOS(ID)
);

CREATE TABLE EXAMENES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CURSO_ID NUMBER NOT NULL,
    TITULO VARCHAR2(150 CHAR) NOT NULL,
    DESCRIPCION VARCHAR2(500 CHAR) NOT NULL,
    PREGUNTAS_JSON CLOB NOT NULL,
    PUNTAJE_MAXIMO NUMBER(10,2) NOT NULL,
    ACTIVO NUMBER(1) DEFAULT 1 NOT NULL,
    FECHA_CREACION TIMESTAMP NOT NULL,
    CONSTRAINT FK_EXAMEN_CURSO
        FOREIGN KEY (CURSO_ID) REFERENCES CURSOS(ID)
);

CREATE TABLE INTENTOS_EXAMEN (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EXAMEN_ID NUMBER NOT NULL,
    ESTUDIANTE_ID NUMBER NOT NULL,
    RESPUESTAS_JSON CLOB NOT NULL,
    FECHA_REALIZACION TIMESTAMP NOT NULL,
    ESTADO VARCHAR2(40 CHAR) NOT NULL,
    PUNTAJE_OBTENIDO NUMBER(10,2),
    NOTA NUMBER(4,2),
    OBSERVACION_PROFESOR VARCHAR2(500 CHAR),
    FECHA_CALIFICACION TIMESTAMP,
    CONSTRAINT FK_INTENTO_EXAMEN
        FOREIGN KEY (EXAMEN_ID) REFERENCES EXAMENES(ID),
    CONSTRAINT FK_INTENTO_ESTUDIANTE
        FOREIGN KEY (ESTUDIANTE_ID) REFERENCES ESTUDIANTES(ID)
);

CREATE TABLE EVENTOS_ACADEMICOS_MQ (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MENSAJE_ID VARCHAR2(120 CHAR) UNIQUE,
    TIPO_EVENTO VARCHAR2(80 CHAR) NOT NULL,
    INSCRIPCION_ID NUMBER,
    ESTUDIANTE_ID NUMBER,
    CURSO_ID NUMBER,
    EXAMEN_ID NUMBER,
    INTENTO_ID NUMBER,
    FECHA_EVENTO TIMESTAMP,
    SERVICIO_ORIGEN VARCHAR2(120 CHAR),
    PAYLOAD_JSON CLOB,
    COLA_ORIGEN VARCHAR2(120 CHAR),
    ESTADO_PROCESAMIENTO VARCHAR2(40 CHAR) NOT NULL,
    DETALLE_ERROR CLOB,
    FECHA_PROCESAMIENTO TIMESTAMP NOT NULL
);
